public class Fib {
    public static void main(String[] args) {
        int i = 0, j = 1, k = 0;
        System.out.println(i);
        k = i + j;
        i = j;
        j = k;

        System.out.println(i);
        k = i + j;
        i = j;
        j = k;

        System.out.println(i);
        k = i + j;
        i = j;
        j = k;

        System.out.println(i);
        k = i + j;
        i = j;
        j = k;

        System.out.println(i);
    }
}

